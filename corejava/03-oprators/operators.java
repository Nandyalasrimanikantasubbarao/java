class operators{
  public static void main(String a[]){

    // arithematic operators
    int num1=7;
    int num2=5;

    // addition
    System.out.println(num1+num2);
    // subtration
    System.out.println(num1-num2);
    // multiplication
    System.out.println(num1*num2);
    // division
    System.out.println(num1/num2);
    // modulo division(remainder)
    System.out.println(num1%num2);
    
    
    // assignment operators
    // = += -= *= /= %=
    // add and asign
    System.out.println(num1+=2);
    // sub and asign
    System.out.println(num1-=num2);
    // mul
    System.out.println(num1*=num2);

  

    // unary opertors(++,--)
    // relation operation(==,!=,<,>,<=,>=)
  }
}
